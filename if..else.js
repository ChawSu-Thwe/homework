var trafficLight = 'yellow';

if (trafficLight == 'red'){
	console.log('STOP');
}else if (trafficLight == 'yellow'){
	console.log('Slow Down');
}else if (trafficLight == 'green'){
	console.log('GO');
}else{
	console.log('Sorry! Something is wrong.');
}



